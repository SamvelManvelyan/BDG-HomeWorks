# Input a two-digit natural number and output the sum of its digits. You can
# assume that the input will be a two-digit number and need not check that
# programmatically.
# Sample Input Sample Output
# 15 6
# 78 15
# 20 2

sample_input = input("Enter Your two-digit int  ")
digit1 = int(sample_input[0])
digit2 = int(sample_input[1])
result = digit1 + digit2

print("Your sum is", + result)